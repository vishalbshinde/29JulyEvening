package Rahul;

public class Switch {
	public static void main(String[] args) {
		switch(3*1)
		{
		case 10/10:
		{
			System.out.println("from case 1");
			break;
		}
		case 2+2:
		{
			System.out.println("from case 2");
			break;
		}
		case 9/3:
		{
			System.out.println("from case 3");
			break;
		}
		default :
		{
			System.out.println("from default value");
		}
		}
	}

}

